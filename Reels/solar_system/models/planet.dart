class Planet {
  final String name;
  final String description;
  final String image;
  final String type;
  final String distanceFromSun;
  final String yearLength;
  final String totalNoOfMoons;

  const Planet({
    required this.name,
    required this.description,
    required this.image,
    required this.type,
    required this.distanceFromSun,
    required this.yearLength,
    required this.totalNoOfMoons,
  });
}
